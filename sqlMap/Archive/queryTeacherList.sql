SELECT T.TEACHERNO,T.ENTERYEAR [YEAR],T.NAME,S2.NAME SEX,P.[VALUE] [POSITION],T2.[VALUE] [TYPE],T.SCHOOL,S.NAME SCHOOLNAME,S1.NAME TGROUP 
FROM TEACHERS T LEFT JOIN POSITIONS P ON T.[POSITION]=P.NAME 
LEFT JOIN TEACHERTYPE T2 ON T.TYPE=T2.NAME 
LEFT JOIN SCHOOLS S ON T.SCHOOL=S.SCHOOL 
LEFT JOIN TGROUPS S1 ON T.TGROUP=S1.TGROUP 
LEFT JOIN SEXCODE S2 ON S2.CODE=T.SEX
WHERE T.TEACHERNO LIKE :TEACHERNO AND T.NAME LIKE :NAME AND T.SEX LIKE :SEX AND T.[POSITION] LIKE :POSITION AND T.SCHOOL LIKE :SCHOOL AND T.TGROUP LIKE :TGROUP
ORDER BY T.TEACHERNO