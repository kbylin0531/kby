SELECT STUDENTS.STUDENTNO as xh,STUDENTS.NAME AS xm,SELECTIONS.TOTALCREDITS AS xf
FROM STUDENTS
LEFT OUTER JOIN
(
  SELECT STUDENTNO,SUM(COURSES.CREDITS) AS TOTALCREDITS
  FROM R32
  JOIN COURSES ON R32.COURSENO=COURSES.COURSENO
  WHERE YEAR=:YEAR
  AND TERM=:TERM
  GROUP BY STUDENTNO
)AS SELECTIONS ON STUDENTS.STUDENTNO=SELECTIONS.STUDENTNO
WHERE STUDENTS.CLASSNO=:CLASSNO
ORDER BY STUDENTS.STUDENTNO

