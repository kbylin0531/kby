SELECT DISTINCT R32.COURSENO AS COURSENOGROUP,COURSES.COURSENAME AS COURSENAME,CAST(COURSES.CREDITS AS CHAR) AS CREDITS
FROM R32 JOIN COURSES ON R32.COURSENO=COURSES.COURSENO
JOIN STUDENTS ON R32.STUDENTNO=STUDENTS.STUDENTNO
WHERE STUDENTS.CLASSNO=:CLASSNO
AND R32.YEAR=:YEAR
AND R32.TERM=:TERM
ORDER BY COURSENOGROUP
