SELECT CLASSROOMS.BUILDING,CLASSROOMS.JSN,ROOMRESERVE.ROOMNO,ROOMRESERVE.DAY,ROOMRESERVE.WEEKS,
ROOMRESERVE.PURPOSE,OEWOPTIONS.NAME AS OEW,
 CONVERT(varchar(10),GETDATE(),20) AS ISSUEDATE,
SCHOOLS.NAME AS SCHOOL,
TIMESECTIONS.VALUE AS TIME,
ROOMRESERVE.YEAR AS YEAR,
ROOMRESERVE.TERM AS TERM
FROM ROOMRESERVE JOIN CLASSROOMS
ON ROOMRESERVE.ROOMNO=CLASSROOMS.ROOMNO
JOIN OEWOPTIONS ON ROOMRESERVE.OEW=OEWOPTIONS.CODE
JOIN SCHOOLS ON ROOMRESERVE.SCHOOL=SCHOOLS.SCHOOL
LEFT OUTER JOIN TIMESECTIONS ON ROOMRESERVE.TIME=TIMESECTIONS.NAME
WHERE ROOMRESERVE.RECNO=:RECNO
AND ROOMRESERVE.APPROVED=1



