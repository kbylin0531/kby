select CLASSROOMS.JSN,SCHEDULE.TIME+SCHEDULE.DAY+SCHEDULE.OEW AS TIME,
RTRIM(COURSES.COURSENAME)+':'+SCHEDULE.COURSENO+SCHEDULE.[GROUP]+'('+TEACHERS.NAME+')' AS COURSE,
SCHEDULE.COURSENO AS COURSENO,
SCHEDULE.[GROUP] AS COURSEGROUP,
SCHEDULE.WEEKS AS WEEKS
FROM  CLASSROOMS inner join SCHEDULE on CLASSROOMS.ROOMNO=SCHEDULE.ROOMNO JOIN COURSES ON SCHEDULE.COURSENO=COURSES.COURSENO
JOIN TEACHERPLAN ON SCHEDULE.MAP=TEACHERPLAN.RECNO
JOIN TEACHERS ON TEACHERPLAN.TEACHERNO=TEACHERS.TEACHERNO
WHERE SCHEDULE.YEAR=:year
AND SCHEDULE.TERM=:term
AND SCHEDULE.ROOMNO=:roomno
ORDER BY TIME
