SELECT ROOMRESERVE.DAY+ROOMRESERVE.OEW AS TIME,
RTRIM(SCHOOLS.NAME)+'借用' AS LEASE,
ROOMRESERVE.TIME AS TIMESECTOR,
ROOMRESERVE.WEEKS AS WEEKS
FROM ROOMRESERVE
JOIN SCHOOLS ON ROOMRESERVE.SCHOOL=SCHOOLS.SCHOOL
WHERE ROOMRESERVE.YEAR=:year
AND ROOMRESERVE.TERM=:term
AND ROOMRESERVE.ROOMNO=:roomno
AND ROOMRESERVE.APPROVED=1
ORDER BY TIME
