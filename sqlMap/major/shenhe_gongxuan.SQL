SELECT ISNULL(SUM(CREDITS),0) AS xf 
FROM ( SELECT DISTINCT SCORES.COURSENO,COURSES.CREDITS  FROM SCORES
INNER JOIN COURSES ON COURSES.COURSENO=SCORES.COURSENO  WHERE (STUDENTNO = :studentno)
AND (SCORES.COURSENO LIKE '08%' OR  SCORES.COURSENO LIKE 'A1%' OR SCORES.COURSENO LIKE '007%'
 OR SCORES.[GROUP] LIKE 'G_')  AND ( SCORES.EXAMSCORE>=60 OR SCORES.TESTSCORE IN ('优秀','良好','中等','及格','合格')
 OR SCORES.TESTSCORE2 IN ('优秀','良好','中等','及格','合格') OR SCORES.EXAMSCORE2>=60)) AS TEMP
