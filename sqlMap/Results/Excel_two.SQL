SELECT STUDENTS.STUDENTNO, STUDENTS.NAME, STUDENTS.SEX,
      STUDENTS.CLASSNO, STUDENTS.TAKEN, STUDENTS.PASSED, STUDENTS.POINTS,
      CLASSES.CLASSNAME, STUDENTS.SCHOOL, SCHOOLS.NAME AS SCHOOLNAME,
      STUDENTS.学位 as xw, STUDENTS.专业 as zy, STUDENTS.论文题目 as lwtm, STUDENTS.指导教师 as zdjs,
      STUDENTS.证书编号 as zsbh,students.毕结业 as bjy,STUDENTS.[学位] as degree
FROM STUDENTS INNER JOIN
      CLASSES ON STUDENTS.CLASSNO = CLASSES.CLASSNO INNER JOIN
      SCHOOLS ON STUDENTS.SCHOOL = SCHOOLS.SCHOOL
WHERE STUDENTS.studentno LIKE :STUDENTNO AND STUDENTS.CLASSNO  LIKE :CLASSNO
ORDER BY studentno