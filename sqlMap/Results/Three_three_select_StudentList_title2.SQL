SELECT distinct teachers.teacherno as teacherno,TEACHERS.NAME AS TEACHERNAME,users.username as username
 FROM SCHEDULEPLAN
JOIN TEACHERPLAN ON SCHEDULEPLAN.RECNO=TEACHERPLAN.MAP
JOIN TEACHERS ON TEACHERPLAN.TEACHERNO=TEACHERS.TEACHERNO inner join users on users.teacherno=teachers.teacherno
LEFT OUTER JOIN TASKOPTIONS ON TEACHERPLAN.TASK=TASKOPTIONS.CODE
WHERE SCHEDULEPLAN.YEAR=:YEAR
AND SCHEDULEPLAN.TERM=:TERM
AND SCHEDULEPLAN.COURSENO+SCHEDULEPLAN.[GROUP]=:COURSENO