INSERT INTO R28
SELECT STUDENTS.STUDENTNO AS STUDENTNO,R16.PROGRAMNO AS PROGRAMNO
FROM CLASSES
JOIN STUDENTS ON CLASSES.CLASSNO=STUDENTS.CLASSNO
JOIN R16 ON CLASSES.CLASSNO=R16.CLASSNO
