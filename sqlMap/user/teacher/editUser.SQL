SELECT users.USERNAME,
users.PASSWORD,
users.DAYSTOLIVE,
users.ROLES,
users.ISDEAN,
CAST(users.PROMPT AS INT) AS PROMPT,
teachers.TEACHERNO,
teachers.NAME AS TEACHERNAME,
teachers.POSITION,
teachers.SCHOOL,
teachers.TGROUP,
teachers.SEX
FROM USERS JOIN TEACHERS ON USERS.TEACHERNO=TEACHERS.TEACHERNO
WHERE users.TEACHERNO = :TEACHERNO
