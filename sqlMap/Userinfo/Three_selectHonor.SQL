SELECT USERS.USERNAME,HONOR.NAME,HONOR.TEACHERNO,CONVERT(varchar(10),HONOR.DATE,20) as DATE,HONOR.DEPARTMENT,
HONORLEVELCODE.NAME AS LEVEL,HONOR.MYORDER,HONOR.RECNO
FROM HONOR INNER JOIN USERS ON USERS.TEACHERNO=HONOR.TEACHERNO
INNER JOIN HONORLEVELCODE ON HONORLEVELCODE.CODE=HONOR.LEVEL
WHERE USERS.USERNAME=:USERNAME